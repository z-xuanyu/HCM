import { app, BrowserWindow } from "electron";
import path from "node:path";
import { checkForUpdate } from "./update";


// The built directory structure
//
// â”œâ”€â”¬â”€â”¬ dist
// â”‚ â”‚ â””â”€â”€ index.html
// â”‚ â”‚
// â”‚ â”œâ”€â”¬ dist-electron
